#include "uSE_Animation.h"


////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////
uSE_Animation::uSE_Animation():
    m_animation_name(""),
    m_nb_frames(0),
    m_framerate_fps(0),
    m_nb_animated_components(0)
{
}


////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////
uSE_Animation::uSE_Animation( const std::string& inAnimationName ):
    m_animation_name(inAnimationName),
    m_nb_frames(0),
    m_framerate_fps(0),
    m_nb_animated_components(0)
{
}


////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////
uSE_Animation::~uSE_Animation()
{
}


////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////
void uSE_Animation::addAnimJoint( const uSE_AnimJoint& inAnimJoint )
{
    m_animjoints.push_back(inAnimJoint);
}


////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////
void uSE_Animation::addBoundBox ( const uSE_BoundBox& inBoundBox )
{
    m_bound_boxes.push_back(inBoundBox);
}


////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////
void uSE_Animation::addBaseFrameJoint( const uSE_Joint& inBaseFrameJoint )
{
    m_base_frame_joints.push_back(inBaseFrameJoint);
}


////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////
void uSE_Animation::addFrameData( const std::vector<float>& inFrameData )
{
    m_frames_components.push_back(inFrameData);
}


////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////
unsigned int uSE_Animation::getNbFrames()
{
    return m_nb_frames;
}


////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////
unsigned int uSE_Animation::getFrameRateFPS()
{
    return m_framerate_fps;
}


////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////
unsigned int uSE_Animation::getNbAnimatedComponents()
{
    return m_nb_animated_components;
}


////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////
unsigned int uSE_Animation::getAnimJointsNb()
{
    return m_animjoints.size();
}


////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////
uSE_AnimJoint uSE_Animation::getAnimJointAtIndex( int inIndex )
{
    return m_animjoints.at(inIndex);
}


////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////
uSE_BoundBox uSE_Animation::getBoundBoxAtIndex( int inIndex )
{
    return m_bound_boxes.at(inIndex);
}


////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////
uSE_Joint uSE_Animation::getBaseFrameJointAtIndex( int inIndex )
{
    return m_base_frame_joints.at(inIndex);
}


////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////
float uSE_Animation::getFrameComponentFromFrameAtIndex( int inFrame, int inIndex )
{
    return m_frames_components.at(inFrame).at(inIndex);
}


////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////
void uSE_Animation::setNbAnimatedComponents( const unsigned int inNbAnimatedComponents )
{
    m_nb_animated_components = inNbAnimatedComponents;
}


////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////
void uSE_Animation::setNbFrames( const unsigned int inNbFrames )
{
    m_nb_frames = inNbFrames;
}


////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////
void uSE_Animation::setFrameRateFPS( const unsigned int inFrameRate )
{
    m_framerate_fps = inFrameRate;
}
