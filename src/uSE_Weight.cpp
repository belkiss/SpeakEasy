#include "uSE_Weight.h"

////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////
uSE_Weight::uSE_Weight():
    m_depending_joint(0),
    m_bias_factor(0)
{
}


////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////
uSE_Weight::uSE_Weight( const int&             inDependingJoint,
                        const float&           inBiasFactor,
                        const Eigen::Vector3f& inPosition ):
    m_depending_joint(inDependingJoint),
    m_bias_factor(inBiasFactor),
    m_position(inPosition)
{
}


////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////
uSE_Weight::~uSE_Weight()
{
}


////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////
int uSE_Weight::getDependingJoint()
{
    return m_depending_joint;
}


////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////
float uSE_Weight::getBiasFactor()
{
    return m_bias_factor;
}


////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////
Eigen::Vector3f uSE_Weight::getPosition()
{
    return m_position;
}
