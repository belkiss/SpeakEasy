#include "uSE_BoundBox.h"


////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////
uSE_BoundBox::uSE_BoundBox()
{
}


////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////
uSE_BoundBox::uSE_BoundBox( const Eigen::Vector3f& inMin,
                            const Eigen::Vector3f& inMax ):
    m_min(inMin),
    m_max(inMax)
{
}


////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////
uSE_BoundBox::~uSE_BoundBox()
{
}


////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////
Eigen::Vector3f uSE_BoundBox::getMin()
{
    return m_min;
}


////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////
Eigen::Vector3f uSE_BoundBox::getMax()
{
    return m_max;
}
