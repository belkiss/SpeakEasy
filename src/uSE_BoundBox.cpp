#include "uSE_BoundBox.h"


////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////
uSE_BoundBox::uSE_BoundBox()
{
}


////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////
uSE_BoundBox::uSE_BoundBox( const uSE_GLVector & inMin,
                            const uSE_GLVector & inMax ):
    m_min(inMin),
    m_max(inMax)
{
}


////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////
uSE_BoundBox::~uSE_BoundBox()
{
}


////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////
uSE_GLVector uSE_BoundBox::getMin()
{
    return m_min;
}


////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////
uSE_GLVector uSE_BoundBox::getMax()
{
    return m_max;
}
