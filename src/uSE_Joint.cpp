#include "uSE_Joint.h"
#include <iostream>

////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////
uSE_Joint::uSE_Joint():
    m_name(""),
    m_parent_index(0)
{
}


////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////
uSE_Joint::uSE_Joint( const std::string&        inName,
                      const int&                inParentIndex,
                      const Eigen::Vector3f&    inPosition,
                      const Eigen::Quaternionf& inOrientation):
    m_name(inName),
    m_parent_index(inParentIndex),
    m_position(inPosition),
    m_orientation(inOrientation)
{
}


////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////
uSE_Joint::~uSE_Joint()
{
}


////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////
void uSE_Joint::setName( const std::string& inName )
{
    m_name = inName;
}


////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////
void uSE_Joint::setParentIndex( const int inParentIndex )
{
    m_parent_index = inParentIndex;
}


////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////
void uSE_Joint::setPosition( const Eigen::Vector3f& inPosition )
{
    m_position = inPosition;
}


////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////
void uSE_Joint::setOrientation( const Eigen::Quaternionf& inOrientation )
{
    m_orientation = inOrientation;
}


////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////
std::string uSE_Joint::getName()
{
    return m_name;
}


////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////
int uSE_Joint::getParentIndex()
{
    return m_parent_index;
}


////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////
Eigen::Vector3f uSE_Joint::getPosition()
{
    return m_position;
}


////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////
Eigen::Quaternionf uSE_Joint::getOrientation()
{
    return m_orientation;
}
